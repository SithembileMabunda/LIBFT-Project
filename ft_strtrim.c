#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int start;
	int len;

	while (!s)
		return (NULL);
	start = 0;
	while (s[start] == ' ' || s[start] == '\n' || s[start] == '\t')
		start++;
	if (s[start] == '\0')
		return (ft_strdup(s + start));
	len = ft_strlen(s) - 1;
	while ((s[len] == ' ' || s[len] == '\n' || s[len] == '\t') && len > 0)
		len--;
	return (ft_strsub(s, start, len - start + 1));
}
