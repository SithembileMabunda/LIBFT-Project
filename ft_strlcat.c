#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t j;
	size_t k;

	i = 0;
	k = 0;
	j = 0;
	while (dst[i] != '\0' && i < dstsize)
		i++;
	while ((src[j] != '\0') && ((i + j + 1) < dstsize))
	{
		dst[i + j] = src[j];
		j++;
	}
	if (i != dstsize)
		dst[i + j] = '\0';
	while (src[k] != '\0')
		k++;
	return (i + k);
}
